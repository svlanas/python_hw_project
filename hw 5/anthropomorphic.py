n = int(input("Введіть число "))
for i in range (1, n):
    # визначаємо кількість розрядів i
    k = 0
    x = i
    while x > 0:
        x //= 10
        k += 1
    # перевіряємо чи число антропоморфне по правилу:
    # для одноцифрових чисел - чи остача від ділення на 10 (одна цифра після коми) дорівнює  i,
    # для двоцифрових чисел - чи остача від ділення на 100 дорівнює i  - і т.д.
    if (i ** 2) % 10 ** k == i:
        print(i)
    i += 1



